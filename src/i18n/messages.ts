export function getMessage(tree: MessageTree, key: string): string | undefined {
  return key.split(".").reduce<string | MessageTree | undefined>((acc, part) => {
    if (!acc || typeof acc === "string") {
      return undefined;
    }
    return acc[part];
  }, tree) as string | undefined;
}
